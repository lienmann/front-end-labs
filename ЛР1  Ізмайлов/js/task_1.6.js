let sum = 0;
let num = parseInt(prompt("Введіть число (введення чисел закінчеться тоді, коли буде введено 0):"));

while (num !== 0) {
    sum += num;
    num = parseInt(prompt("Введіть ще число (введення чисел закінчеться тоді, коли буде введено 0):"));
}

alert("Сума введених чисел: " + sum);
